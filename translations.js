// Current language
let currentLang = localStorage.getItem('mvpl-lang') || 'es';

/**
 * Get translation by key
 */
function t(key) {
  return translations[currentLang][key] || key;
}

/**
 * Set language and update UI
 */
function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('mvpl-lang', lang);
  updatePageTranslations();
  
  // Update language buttons
  document.querySelectorAll('.header__lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

/**
 * Update all translations on the page
 */
function updatePageTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const translation = t(key);
    
    if (el.tagName === 'INPUT' && el.type === 'placeholder') {
      el.placeholder = translation;
    } else {
      el.innerHTML = translation;
    }
  });
}

/**
 * Initialize translations
 */
function initTranslations() {
  // Set initial language from storage or default
  const savedLang = localStorage.getItem('mvpl-lang') || 'es';
  setLanguage(savedLang);
  
  // Add click handlers to language buttons
  document.querySelectorAll('.header__lang-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      setLanguage(btn.dataset.lang);
    });
  });
}

export { t, setLanguage, initTranslations, currentLang };
